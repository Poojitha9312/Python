# List Built-in-Functions are:
# sum()
# Maximum(max(list))
# Minimum(min(list))
# Sequence

# 1.sum():
# It returns the sum of all elements in a list
p1=[1,2,3,4]
print(sum(p1))

# 2.Maximum(max(list)):
# Here it returns the maximum element present in the list
p1=[1,2,3,4,5,6]
print(max(p1))

# 3.Minimum(min(list)):
# Here it returns the minimum element present in the list
p1=[1,2,3,4,5]
print(min(p1))

# 4.Sequence:
# It converts any sequence to list
p1="Python"
print(list(p1))
